# Задача-1:
# Дан список, заполненный произвольными целыми числами, получите новый список,
# элементами которого будут квадратные корни элементов исходного списка,
# но только если результаты извлечения корня не имеют десятичной части и
# если такой корень вообще можно извлечь
# Пример: Дано: [2, -5, 8, 9, -25, 25, 4]   Результат: [3, 5, 2]

try:
    num_lst = input("Введите список целых чисел, рязделяя элементы запятыми или пробелами: ")
    num_lst = list(map(int, num_lst.replace(',', ' ').split()))
    print(f"Дано: {num_lst}")
    num_lst_new = []
    for num in num_lst:
        if num >= 0:  # Если из числа можно извлечь не комплексный квадратный корень
            square_root = num ** 0.5
            if square_root - int(square_root) == 0:  # Если результаты извлечения корня не имеют десятичной части
                num_lst_new.append(int(square_root))
    print(f"Результат: {num_lst_new}")
except ValueError:
    print("Вы ввели неверные данные")


