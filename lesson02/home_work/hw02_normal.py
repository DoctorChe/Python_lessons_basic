# Задача-4: Дан список, заполненный произвольными целыми числами.
# Получите новый список, элементами которого будут: 
# а) неповторяющиеся элементы исходного списка:
# например, lst = [1, 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 2, 4, 5, 6]
# б) элементы исходного списка, которые не имеют повторений:
# например, lst = [1 , 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 4, 6]

try:
    num_lst = input("Введите список целых чисел, рязделяя элементы запятыми или пробелами: ")
    num_lst = list(map(int, num_lst.replace(',', ' ').split()))
    print(f"Исходный список: {num_lst}")

    lst_a = list(set(num_lst))
    print(f"а) неповторяющиеся элементы исходного списка: {lst_a}")

    lst_b = [x for x in num_lst if num_lst.count(x) == 1]
    print(f"б) элементы исходного списка, которые не имеют повторений: {lst_b}")
except ValueError:
    print("Вы ввели неверные данные")
